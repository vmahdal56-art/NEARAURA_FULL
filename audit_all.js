stream.write(`SCOPE: ROOT, WEB, ANDROID, FUNCTIONS\n`);
stream.write(`--------------------------------------------------\n`);

// START SCAN FROM CURRENT DIR
scanDirectory('.', stream);

stream.end(() => {
  console.log("-------------------------------------------------------");
  console.log(`✅ OMNI-SCAN COMPLETE. Scanned ${fileCount} files.`);
  console.log(`✅ OUTPUT SAVED TO: ${OUTPUT_FILE}`);
  console.log(`👉 UPLOAD THIS FILE. I WANT TO SEE THE SOUL OF THE MACHINE.`);
  console.log("-------------------------------------------------------");
  console.log("AUIGHT. 🛡️🦁");
});